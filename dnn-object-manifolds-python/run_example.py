#!/usr/bin/env python3
"""
End-to-end pipeline for manifold analysis.

Implements the pipeline from the MATLAB master scripts:
  - generate_datasets_ncomms2020.m   (dataset generation)
  - generate_results_ncomms2020.m    (analysis: capacity + geometry)

The MATLAB pipeline uses file-based I/O with .mat metadata files generated by
generate_convnet_model_metadata.m and load_network_metadata.m.  Since those
metadata files are tightly coupled to MATLAB conventions, this script instead
calls the core library functions directly (check_binary_dichotomies_capacity,
optimal_low_rank_structure, calc_manifold_properties) on data generated
in-process via PyTorch.

Usage:
  # Small test: AlexNet, 10 objects, 15 samples, 1 direction
  python run_example.py --network_type 1 --n_objects 10 --n_samples 15 \\
      --range_factor 1.0 --directions 0 --output_dir ./test_results

  # Full pipeline: AlexNet, all defaults
  python run_example.py --network_type 1 --output_dir ./results

Network types: 1=alexnet, 3=resnet50, 5=vgg16
"""

import argparse
import math
import os
import sys
import time

import numpy as np

sys.path.insert(0, os.path.dirname(os.path.abspath(__file__)))

from smooth_manifolds_generation.config import (
    IMAGENET_IMAGE_SIZE,
    IMAGENET_FRAME_SIZE,
    N_HMAX_FEATURES,
)

# ============================================================================
# Constants matching MATLAB generate_results_ncomms2020.m
# ============================================================================

RANGE_FACTORS = [0.125, 0.25, 0.5, 1, 2, 4, 8, 12, 16]
SAMPLES_1D = [5, 9, 15, 27, 51, 101, 201, 201, 201]

DIRECTION_NAMES = [
    'x-translation', 'y-translation', 'x-scale', 'y-scale',
    'x-shear', 'y-shear', 'rotation',
]

# Analysis parameters matching MATLAB defaults
EXPECTED_PRECISION = 0.05
MAX_SAMPLES = 100
FEATURES_TYPE = 2  # 0=sub-sample, 2=random projections
OPTIMIZATION_N_REPEATS = 10


# ============================================================================
# Step 1: Generate tuning functions via PyTorch
# ============================================================================

def generate_tuning_functions(network_type, n_objects, n_samples, range_factor,
                               directions, verbose=True):
    """
    Generate 1D manifold tuning functions:
      1. Create random images (stand-in for ImageNet thumbnails)
      2. Apply 1D affine transforms per direction/sample
      3. Extract features from each network layer via PyTorch

    Returns dict: layer_name -> (N_DIRECTIONS, n_objects, n_samples, n_features)
    """
    import torch
    from smooth_manifolds_generation.feature_extraction import (
        convnet_init, preprocess_image, NETWORK_TYPE_STRINGS,
    )
    from smooth_manifolds_generation.affine_transforms import (
        create_1d_affine_transform,
        calc_imagenet_warp_legacy,
    )

    network_name = NETWORK_TYPE_STRINGS.get(network_type, f'net{network_type}')
    print(f'\n=== Generating tuning functions ===')
    print(f'  network={network_name}, P={n_objects}, M={n_samples}, '
          f'range={range_factor}, dirs={directions}')

    # Load model
    model, input_size, norm_info = convnet_init(network_type)
    device = next(model.parameters()).device
    model.eval()

    # Register hooks on all leaf modules to capture activations
    activations = {}
    hooks = []
    for name, module in model.named_modules():
        if len(list(module.children())) == 0:  # leaf
            def _hook(mod, inp, out, n=name):
                if isinstance(out, torch.Tensor):
                    activations[n] = out.detach().cpu()
            hooks.append(module.register_forward_hook(_hook))

    # Probe to get layer sizes
    rng = np.random.default_rng(42)
    probe = np.zeros((IMAGENET_IMAGE_SIZE, IMAGENET_IMAGE_SIZE, 3), dtype=np.uint8)
    probe_t = preprocess_image(probe, input_size, norm_info).to(device)
    with torch.no_grad():
        model(probe_t)

    layer_info = {}  # name -> n_features
    for name, act in activations.items():
        n_feat = int(np.prod(act.shape[1:]))
        layer_info[name] = n_feat

    if verbose:
        print(f'  {len(layer_info)} layers detected')

    # Create random images
    base_images = rng.integers(
        0, 256,
        size=(n_objects, IMAGENET_FRAME_SIZE, IMAGENET_FRAME_SIZE, 3),
        dtype=np.uint8,
    )

    N_DIRECTIONS = len(DIRECTION_NAMES)

    # Build tuning functions per layer
    results = {}
    for lname, n_feat in layer_info.items():
        # Subsample features if too many
        n_keep = min(n_feat, N_HMAX_FEATURES)
        if n_feat > N_HMAX_FEATURES:
            feat_idx = rng.choice(n_feat, size=N_HMAX_FEATURES, replace=False)
            feat_idx.sort()
        else:
            feat_idx = np.arange(n_feat)
        results[lname] = {
            'tf': np.zeros((N_DIRECTIONS, n_objects, n_samples, n_keep), dtype=np.float32),
            'feat_idx': feat_idx,
        }

    t0 = time.time()
    n_total = len(directions) * n_objects * n_samples
    n_done = 0

    for di in directions:
        for oi in range(n_objects):
            for si in range(n_samples):
                # param_id is 1-based (1-7), j is 0-based sample index
                T = create_1d_affine_transform(range_factor, di + 1, si, n_samples)
                warped = calc_imagenet_warp_legacy(base_images[oi], T)

                # Ensure 3-channel
                if warped.ndim == 2:
                    warped = np.stack([warped] * 3, axis=-1)
                elif warped.shape[-1] == 1:
                    warped = np.concatenate([warped] * 3, axis=-1)

                img_t = preprocess_image(
                    warped.astype(np.float32), input_size, norm_info
                ).to(device)

                activations.clear()
                with torch.no_grad():
                    model(img_t)

                for lname, info in results.items():
                    if lname in activations:
                        feat = activations[lname].numpy().ravel()
                        info['tf'][di, oi, si, :] = feat[info['feat_idx']]

                n_done += 1
                if verbose and n_done % 50 == 0:
                    elapsed = time.time() - t0
                    rate = n_done / elapsed if elapsed > 0 else 0
                    print(f'    {n_done}/{n_total} ({rate:.1f} img/s)')

    # Clean up hooks
    for h in hooks:
        h.remove()

    elapsed = time.time() - t0
    if verbose:
        print(f'  Generation complete ({elapsed:.1f}s)')

    # Return just the tuning functions
    layer_tfs = {}
    for lname, info in results.items():
        layer_tfs[lname] = info['tf']

    return layer_tfs, network_name


