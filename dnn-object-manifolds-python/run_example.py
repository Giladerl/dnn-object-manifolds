# Step 2: Capacity analysis (core library calls)
# ============================================================================

def run_capacity_analysis(layer_tfs, n_objects, directions, verbose=True):
    """
    Run capacity analysis on each layer's tuning functions.

    For each layer, for each direction:
      1. Extract (N_NEURONS, N_SAMPLES, N_OBJECTS) tuning function
      2. Remove zero-firing neurons
      3. Call check_binary_dichotomies_capacity

    Returns dict with all MATLAB-matching result variables.
    """
    from library.check_binary_dichotomies_capacity import check_binary_dichotomies_capacity

    layer_names = list(layer_tfs.keys())
    N_LAYERS = len(layer_names)
    N_DIRECTIONS = len(DIRECTION_NAMES)

    # MATLAB result arrays
    capacity_results = np.full((N_DIRECTIONS, N_LAYERS), np.nan)
    separability_results = {}  # per (di, li)
    neuron_samples_used_results = {}
    n_support_vectors_results = {}

    print(f'\n=== Capacity analysis ({N_LAYERS} layers) ===')
    rng = np.random.default_rng(0)

    for li, lname in enumerate(layer_names):
        tf_4d = layer_tfs[lname]  # (N_DIRS, n_objects, n_samples, n_features)
        n_features = tf_4d.shape[3]

        for di in directions:
            # Extract: (n_objects, n_samples, n_features) -> (n_features, n_samples, n_objects)
            tf_slice = tf_4d[di, :n_objects, :, :].astype(np.float64)
            full_tf = tf_slice.transpose(2, 1, 0)  # (N_NEURONS, N_SAMPLES, N_OBJECTS)

            # Remove zero-firing neurons
            mean_sq = np.mean(np.mean(full_tf ** 2, axis=2), axis=1)
            nz = np.where(mean_sq > 0)[0]
            if len(nz) == 0:
                if verbose:
                    print(f'  {lname} dir={DIRECTION_NAMES[di]}: all zeros, skipping')
                continue

            current_tf = full_tf[nz, :, :]
            N = current_tf.shape[0]

            if verbose:
                print(f'  {lname} dir={DIRECTION_NAMES[di]}: '
                      f'N={N} neurons, shape={current_tf.shape}')

            try:
                t0 = time.time()
                cap, sep, neuron_counts, nsu, nsv = check_binary_dichotomies_capacity(
                    current_tf,
                    EXPECTED_PRECISION=EXPECTED_PRECISION,
                    max_samples=MAX_SAMPLES,
                    random_labeling_type=1,
                    features_type=FEATURES_TYPE,
                    verbose=int(verbose),
                    rng=rng,
                )
                elapsed = time.time() - t0

                capacity_results[di, li] = cap
                separability_results[(di, li)] = sep
                neuron_samples_used_results[(di, li)] = nsu
                n_support_vectors_results[(di, li)] = nsv

                if verbose:
                    print(f'    capacity={cap:.3f} ({elapsed:.1f}s)')

            except Exception as e:
                print(f'    FAILED: {e}')
                import traceback
                traceback.print_exc()

    return {
        'capacity_results': capacity_results,
        'separability_results': separability_results,
        'neuron_samples_used_results': neuron_samples_used_results,
        'n_support_vectors_results': n_support_vectors_results,
        'EXPECTED_PRECISION': EXPECTED_PRECISION,
        'max_samples': MAX_SAMPLES,
        'precision': 1,
        'features_type': FEATURES_TYPE,
        'layer_names': layer_names,
    }


